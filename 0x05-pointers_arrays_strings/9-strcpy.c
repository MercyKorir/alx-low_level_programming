#include <stdio.h>
#include <string.h>
#include "main.h"

/**
* _strcpy - copies text from source to destination
* @dest: pointer where the text will be copied to
* @src: source where text will be copied from
* Return: the pointer to dest.
*/
char *_strcpy(char *dest, char *src)
{
	strcpy(dest, src);
}
